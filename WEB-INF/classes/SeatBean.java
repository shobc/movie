
public class SeatBean{

    private String denomination;
    private String payment;
    private String automatic_number;
    private int pay_flag;

    public String getDenomination() {
        return denomination;
    }
    public void setDenomination(String denomination) {
        this.denomination = denomination;
    }

    public String getPayment() {
        return payment;
    }
    public void setPayment(String payment) {
        this.payment = payment;
    }

    public String getAutomatic_number() {
        return automatic_number;
    }
    public void setAutomatic_number(String automatic_number) {
        this.automatic_number = automatic_number;
    }

    public int getPay_flag() {
        return pay_flag;
    }
    public void setPay_flag(int pay_flag) {
        this.pay_flag = pay_flag;
    }

}